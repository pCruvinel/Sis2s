export const filterByEmpresa = (items: any[], empresaId: string | undefined) => {
  if (!empresaId) return items;
  if (empresaId === 'grupo') return items; // Grupo vê tudo
  return items.filter(item => {
    // Se o item tem empresa_id direto
    if (item.empresa_id === empresaId) return true;
    // Se o item tem lista de empresas (colaborador)
    if (Array.isArray(item.empresas) && item.empresas.includes(empresaId)) return true;
    // Se o item tem rateio que inclui a empresa
    if (item.rateio && Object.keys(item.rateio).includes(empresaId)) return true;
    
    return false;
  });
};
